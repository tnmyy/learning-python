# Data Types in Python

print(type(a))  # Number
print(type(b))  # String
print(type(c))  # Boolean
print(type(d))  # None

e = 69.69
print(type(e))  # Float

f = (8, 1)
print(type(f))  # Complex Number/Tuple (Real, Imaginary)


list = [69, 96, "Tanmay", None, 9.2, (8.09), [1, 2, 3]]
print(type(list))  # list - mutable

tuple = (69, 96, "Tanmay", None, 9.2, (8.09))
print(type(tuple))  # tuple - immutable

dictionary = {"Name": "Tanmay", "Percentage": 94.6}
print(type(dictionary))
print(dictionary)

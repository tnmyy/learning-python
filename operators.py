# Operators in Python


num1 = 10
num2 = 3

# Add

print(num1 + num2)

# Subtract

print(num1 - num2)

# Multiply

print(num1 * num2)

# Divide

print(num1 / num2)

# Module (Gives Remainder)

print(num1 % num2)

# Floor Division (Gives Quotient)

print(num1 // num2)
print(num1 // 5)

# Exponential

print(num1**num2)  # num1 to the power num2


